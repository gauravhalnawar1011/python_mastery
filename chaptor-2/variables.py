# Variable Declarations with Data Types

my_int = 1                 # Integer data type
my_float = 3.12            # Float (decimal) data type
my_string = "hello devops"  # String (text) data type

# Assigning one variable to another
reference_value = "This is the reference"  # This variable holds a reference value
assigned_value = reference_value           # This variable gets the value from reference_value

# Boolean data type
my_boolean_True = True     # Boolean values are either True or False

my_boolean_False = False

# None data type (represents the absence of a value)
my_none = None

